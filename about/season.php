<h2>The Season</h2>
<hr>
<p class="contentBig">
    The True Minded Gaming League consists of 14 teams. Matches are in a Best-of-3 format, with each team (except two, which have byes) playing, for a total of 6 matches per weekend. The schedule is randomized so each team should play every other team once. All games will be streamed with
    experienced casters on our <a href="http://www.twitch.tv/truemindedgaming">Twitch channel</a>.
    <br><br>
    After 12 weeks of regular season, the top eight teams will continue onto Playoffs. Playoffs will follow a Best-of-5 format, and seeds will play opposite seeds (ie. 1vs8, 2vs7, etc.).
</p>